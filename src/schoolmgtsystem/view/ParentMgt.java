        ResultSet rs = null;

        if (!id.isEmpty()) {
            String str = "SELECT * FROM parent_details WHERE ParentID = ?";
            try {
                PreparedStatement pst = handler.getdbConnection().prepareStatement(str);
                pst.setString(1, id);
                int counter = 0;
                rs = pst.executeQuery();
                if (rs.next()) {
                    counter++;
                    name = rs.getString("ParentName");
                    email = rs.getString("Email");
                    pass = rs.getString("Password");
                    home = rs.getString("HomeAddress");
                    office = rs.getString("OfficeAddress");
                    occupation = rs.getString("Occupation");
                    nation = rs.getString("Nationality");
                    state = rs.getString("State");
                    tel1 = rs.getString("Tel1");
                    tel2 = rs.getString("Tel2");
                    rel = rs.getString("Religion");
                } else {
                    JOptionPane.showMessageDialog(this, "No Parent ID as " + id);
                    JTextField temp = null;
                    for (Component C : jPanel1.getComponents()) {
                        if (C.getClass().toString().contains("javax.swing.JTextField")) {
                            temp = (JTextField) C;
                            temp.setText(null);
                        }
                    }
                    txtName.setText("");
                    txtOccupation.setText("");
                    txtHome.setText("");
                    txtEmail.setText("");
                    txtPassword.setText("");
                    txtTel1.setText("");
                    txtTel2.setText("");
                    txtOffice.setText("");
                    txtNation.setText("");
                    txtReligion.setText("");
                    txtState.setText("");
                }
                if (counter == 1) {
                    txtName.setText(name);
                    txtEmail.setText(email);
                    txtPassword.setText(pass);
                    txtHome.setText(home);
                    txtOffice.setText(office);
                    txtOccupation.setText(occupation);
                    txtNation.setText(nation);
                    txtState.setText(state);
                    txtTel1.setText(tel1);
                    txtTel2.setText(tel2);
                    txtReligion.setText(rel);

                    lblError.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Can't retrieve data !");
                }

            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ParentMgt.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            lblError.setText("ID is needed !");
            txtName.setText("");
            txtOccupation.setText("");
            txtHome.setText("");
            txtEmail.setText("");
            txtPassword.setText("");
            txtTel1.setText("");
            txtTel2.setText("");
            txtOffice.setText("");
            txtNation.setText("");
            txtReligion.setText("");
            txtState.setText("");
        }

